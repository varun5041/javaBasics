package com.basics;

public class loops {
    public static void main(String[] args) {
        //for loop
        for(int i=1;i<=5;i++)
        {
            System.out.println(i);
        }

        //while loop
        int i=1;
        while(i<=5)
        {
            System.out.println(i);
            i++;
        }
    }
}
